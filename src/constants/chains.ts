// Helper function to get available tokens for a specific chain
export function getTokensForChain(chainId: number): string[] {
  const tokens = TOKEN_ADDRESSES[chainId as keyof typeof TOKEN_ADDRESSES];
  return tokens ? Object.keys(tokens) : [];
}

// Helper function to get token address for a specific chain and symbol
